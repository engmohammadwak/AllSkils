def slugify(text):
    text = text.lower()
    text = re.sub(r'[^a-z0-9\-_]+', '-', text)
    text = re.sub(r'-+', '-', text)
    if len(text) > 80:
        text = text[:80]
        text = text.rstrip('-')
    return text.strip('-')


def extract_skill_name(content, fallback_name):
    match = re.search(r'^name:\s*(.+)$', content, re.MULTILINE)
    if match:
        name = match.group(1).strip()
        name = re.sub(r'\s*\(\d+\)\s*$', '', name)
        return name
    lines = content.split('\n')
    for line in lines[:20]:
        line = line.strip()
        if not line or line.startswith('#') or line.startswith('```') or line.startswith('---'):
            continue
        clean_name = re.sub(r'^#\s*', '', line)
        clean_name = re.sub(r'\s*\(skill\)$', '', clean_name, flags=re.IGNORECASE)
        clean_name = re.sub(r'\s*\(\d+\)\s*$', '', clean_name)
        if clean_name and len(clean_name) > 3:
            return clean_name.strip()
    clean_name = re.sub(r'^SKILL\s*\(?\d+\)?\s*-\s*', '', fallback_name)
    clean_name = re.sub(r'\s*\(\d+\)\s*$', '', clean_name)
    clean_name = re.sub(r'^SKILL\s+', '', clean_name)
    clean_name = re.sub(r'^SKILL-', '', clean_name)
    if clean_name and clean_name != fallback_name:
        return clean_name.strip()
    return fallback_name


def extract_description(content):
    match = re.search(r'^description:\s*(.+)$', content, re.MULTILINE | re.IGNORECASE)
    if match:
        return match.group(1).strip()
    lines = content.split('\n')
    for i, line in enumerate(lines):
        if re.search(r'^#+\s*(description|overview|about|summary)', line, re.IGNORECASE):
            if i + 1 < len(lines) and lines[i + 1].strip():
                return lines[i + 1].strip()
    return ""


def detect_main_category(text, skill_name=""):
    text_lower = text.lower()
    skill_lower = skill_name.lower()
    scores = {}
    for category, keywords in MAIN_CATEGORY_KEYWORDS.items():
        score = 0
        for keyword in keywords:
            if keyword.lower() in skill_lower:
                score += 5
            score += text_lower.count(keyword.lower()) * 2
        if score > 0:
            scores[category] = score
    if not scores:
        return None
    return max(scores, key=scores.get)


def detect_sub_category(text, main_category, skill_name=""):
    if not main_category or main_category not in SUB_CATEGORIES:
        return None
    text_lower = text.lower()
    skill_lower = skill_name.lower()
    sub_categories = SUB_CATEGORIES[main_category]
    scores = {}
    for sub_id, sub_data in sub_categories.items():
        score = 0
        for keyword in sub_data["keywords"]:
            if keyword.lower() in skill_lower:
                score += 5
            score += text_lower.count(keyword.lower()) * 2
        if score > 0:
            scores[sub_id] = score
    if not scores:
        return None
    return max(scores, key=scores.get)


def detect_technology(text, main_category, sub_category, skill_name=""):
    """
    Detect the specific technology/framework from the content.
    Returns: technology name or "general" if not found.
    Path structure: maincategory / subcategory / technology
    """
    text_lower = text.lower()
    skill_lower = skill_name.lower()
    scores = {}
    for tech_name, tech_data in TECH_STACKS.items():
        if tech_data["category"] != main_category:
            continue
        if tech_data["subcategory"] != sub_category:
            continue
        score = 0
        for keyword in tech_data["keywords"]:
            keyword_lower = keyword.lower()
            if keyword_lower in skill_lower:
                score += 10
            score += text_lower.count(keyword_lower) * 3
        if score > 0:
            scores[tech_name] = score
    if not scores:
        return "general"
    return max(scores, key=scores.get)


def get_file_hash(content):
    return hashlib.sha256(content.encode('utf-8')).hexdigest()


def get_file_signature(content, file_size):
    normalized = re.sub(r'\s+', ' ', content.strip())
    content_sig = normalized[:500]
    return f"{file_size}|{content_sig}"


def get_unique_filename(base_name, full_path, output_dir, used_names):
    if len(base_name) > 60:
        base_name = base_name[:60]
    clean_name = slugify(base_name)
    if not clean_name or len(clean_name) < 2:
        clean_name = "unnamed"
    if len(clean_name) > 50:
        clean_name = clean_name[:50].rstrip('-')
    filename = clean_name + ".md"
    counter = 1
    dest_dir = output_dir / full_path
    max_attempts = 50
    while counter < max_attempts:
        filename = f"{clean_name}-{counter}.md" if counter > 1 else f"{clean_name}.md"
        full_path_check = dest_dir / filename
        if len(str(full_path_check)) > 240:
            clean_name = clean_name[:30].rstrip('-')
            filename = f"{clean_name}-{counter}.md"
            full_path_check = dest_dir / filename
        if not (dest_dir / filename).exists() and filename not in used_names:
            break
        counter += 1
    if counter >= max_attempts:
        import time
        filename = f"skill-{int(time.time())}.md"
        while (dest_dir / filename).exists() or filename in used_names:
            import time
            filename = f"skill-{int(time.time())}.md"
    used_names.add(filename)
    return filename


