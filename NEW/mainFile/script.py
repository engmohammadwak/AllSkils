def process_file(md_file, output_dir, existing_hashes, used_names, signatures):
    """
    Process a single markdown file and copy it to the correct folder.
    Folder structure: output_dir / main_category / sub_category / technology / filename.md
    """
    try:
        content = md_file.read_text(encoding="utf-8", errors="ignore")
        file_hash = get_file_hash(content)

        # Skip duplicates
        if file_hash in existing_hashes:
            return None, "duplicate"

        file_size = md_file.stat().st_size
        sig = get_file_signature(content, file_size)

        skill_name = extract_skill_name(content, md_file.stem)
        description = extract_description(content)

        if not skill_name:
            skill_name = md_file.stem

        main_category = detect_main_category(content, skill_name)

        if not main_category:
            log_unclassified_file(
                md_file.name, skill_name,
                lang.get('no_category_found'),
                details=content[:200]
            )
            return None, "no_category"

        sub_category = detect_sub_category(content, main_category, skill_name)

        if not sub_category:
            log_unclassified_file(
                md_file.name, skill_name,
                lang.get('no_subcategory_found', main_category),
                main_category=main_category,
                details=content[:200]
            )
            return None, "no_subcategory"

        # 🔧 Detect the specific technology/framework
        technology = detect_technology(content, main_category, sub_category, skill_name)

        # Build the 3-level path: main / sub / technology
        folder_path = Path(main_category) / sub_category / technology

        filename = get_unique_filename(skill_name, folder_path, output_dir, used_names)
        dest_dir = output_dir / folder_path
        dest_dir.mkdir(parents=True, exist_ok=True)

        dest_path = dest_dir / filename
        shutil.copy2(md_file, dest_path)

        # Save signature
        signatures[file_hash] = {
            "filename": filename,
            "path": str(folder_path / filename),
            "skill_name": skill_name,
            "technology": technology
        }
        existing_hashes.add(file_hash)

        return {
            "skill_name": skill_name,
            "description": description,
            "main_category": main_category,
            "sub_category": sub_category,
            "technology": technology,
            "file_path": str(folder_path / filename)
        }, "success"

    except Exception as e:
        print(Fore.RED + lang.get('error_processing', md_file.name, str(e)))
        return None, "error"